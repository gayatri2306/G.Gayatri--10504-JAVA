import java.util.Scanner;
public class Simpleinterest{
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int p = sc.nextInt();
        int r = sc.nextInt();
        int t = sc.nextInt();
        int si = (p*r*t)/100;
        if(si>100){
            System.out.println("High interest");
        }
        else{
            System.out.println("Low interest");
        }
    }
}


