import java.util.Scanner;
public class Profitloss{
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int sell = sc.nextInt();
        int cost = sc.nextInt();
        if(sell>cost){
            System.out.println("Profit");
        }
        else if(sell<cost){
            System.out.println("Loss");
        }
        else{
            System.out.println("No profit no loss");
        }
    }
}


